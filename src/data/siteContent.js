function defaultPack(label) {
  return pack(
    [
      `Разделът „${label}“ присъства в официалната карта на сайта на Община Пещера и се поддържа за навигация и информиране на потребителите.`,
      ...stdFooter,
    ],
    [`Пълно съдържание: ${OFFICIAL_SITE}`],
  );
}

/**
 * Текстово съдържание за страница по път и възел от менюто.
 */
export function getPageContent(pathname, node) {
  const title = node?.label ?? 'Страница';
  const fromExplicit = EXPLICIT[pathname];
  if (fromExplicit) return { title, ...fromExplicit };

  const fromPattern = prefixBlock(pathname, title);
  if (fromPattern) return { title, ...fromPattern };

  return { title, ...defaultPack(title) };
}

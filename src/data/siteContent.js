function prefixBlock(pathname, label) {
  if (pathname === '/') return null;
  if (/^\/razdel\/os-pk-[^/]+\/dnevni-redove$/.test(pathname)) {
    return pack(
      [
        `В раздела „${label}“ се публикуват проекти за дневен ред на съответната постоянна комисия към Общинския съвет — Пещера.`,
        'Подредбата и окончателният дневен ред се утвърждават съгласно правилника за дейност на Общинския съвет.',
        ...stdFooter,
      ],
      ['За актуални файлове и дати на заседания следете www.peshtera.bg.'],
    );
  }
  if (/^\/razdel\/os-pk-[^/]+\/materiali$/.test(pathname)) {
    return pack(
      [
        `Тук са достъпни материалите за предстоящо заседание на комисията — доклади, проекти за решения и приложения.`,
        ...stdFooter,
      ],
      [],
    );
  }
  if (/^\/razdel\/os-pk-[^/]+\/protokoli$/.test(pathname)) {
    return pack(
      [
        `Разделът съдържа протоколи от заседания на постоянната комисия, приети в рамките на съответния мандат.`,
        ...stdFooter,
      ],
      [],
    );
  }
  if (/^\/razdel\/os-resheniya\/mandat-/.test(pathname)) {
    return pack(
      [
        `Решенията на Общинския съвет — Пещера за избрания мандатен период са публикувани задължително за обществен достъп.`,
        ...stdFooter,
      ],
      ['Търсене по номер и дата — на официалния сайт.'],
    );
  }
  if (/^\/razdel\/os-protokoli\/mandat-/.test(pathname)) {
    return pack(
      [
        'Протоколите от заседания на Общинския съвет удостоверяват взетите решения и начина на гласуване.',
        ...stdFooter,
      ],
      [],
    );
  }
  if (/^\/razdel\/ee-lot-\d+$/.test(pathname)) {
    return pack(
      [
        `Обектът е част от проектите за енергийна ефективност на общината (етап/лот „${label}“).`,
        'Технически спецификации, обществени поръчки и отчетност се публикуват на официалния портал.',
        ...stdFooter,
      ],
      [],
    );
  }
  if (/^\/razdel\/ee-bg-rrp-4-023-|^\/razdel\/ee-mf-bg-rrp-4-023-/.test(pathname)) {
    return pack(
      [
        `Страницата отразява проектно предложение по идентификатор „${label}“ в рамките на националното енергийно обновяване на жилищни сгради.`,
        ...stdFooter,
      ],
      [],
    );
  }
  if (/^\/razdel\/ee-[^/]+$/.test(pathname) && pathname.includes('ee-')) {
    return pack(
      [
        `Разделът „${label}“ описва обект или компонент от програмите за енергийна ефективност на Община Пещера.`,
        ...stdFooter,
      ],
      [],
    );
  }
  if (/^\/razdel\/os-pk-[^/]+$/.test(pathname)) {
    return pack(
      [
        `Постоянната комисия „${label}“ подпомага работата на Общинския съвет. Подстраниците съдържат проекти за дневни редове, материали за заседания и протоколи.`,
        ...stdFooter,
      ],
      [],
    );
  }
  return null;
}

function defaultPack(label) {
  return pack(
    [
      `Разделът „${label}“ присъства в официалната карта на сайта на Община Пещера и се поддържа за навигация и информиране на потребителите.`,
      ...stdFooter,
    ],
    [`Пълно съдържание: ${OFFICIAL_SITE}`],
  );
}

/**
 * Текстово съдържание за страница по път и възел от менюто.
 */
export function getPageContent(pathname, node) {
  const title = node?.label ?? 'Страница';
  const fromExplicit = EXPLICIT[pathname];
  if (fromExplicit) return { title, ...fromExplicit };

  const fromPattern = prefixBlock(pathname, title);
  if (fromPattern) return { title, ...fromPattern };

  return { title, ...defaultPack(title) };
}
